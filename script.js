// Função para popular as listas suspensas
function popularListas() {
    const temperaturaSelect = document.getElementById('temperatura');
    const pressaoSelect = document.getElementById('pressao');

    // Extrai valores únicos de temperatura e pressão
    const temperaturas = [...new Set(tabelaVolumeGas.map(item => item.temperatura))];
    const pressoes = [...new Set(tabelaVolumeGas.map(item => item.pressao))];

    // Popula a lista de temperaturas
    temperaturas.forEach(temp => {
        const option = document.createElement('option');
        option.value = temp;
        option.text = temp + '°C';
        temperaturaSelect.appendChild(option);
    });

    // Popula a lista de pressões
    pressoes.forEach(press => {
        const option = document.createElement('option');
        option.value = press;
        option.text = press + ' PSI';
        pressaoSelect.appendChild(option);
    });
}

    
  // Função para atualizar a tabela de registros
function atualizarTabela() {
    let registros = JSON.parse(localStorage.getItem('registros')) || [];
    
    // Atribui os IDs de forma decrescente (o mais recente com maior ID)
    const totalRegistros = registros.length;

    const tabela = document.getElementById('tabela-registros').getElementsByTagName('tbody')[0];
    tabela.innerHTML = ''; // Limpa a tabela

    // Insere os registros na tabela com IDs decrescentes, do mais recente para o mais antigo
    registros.forEach((registro, index) => {
        const novaLinha = tabela.insertRow(0); // Insere a nova linha no topo da tabela
        
        // Atribui o ID de forma decrescente (registro mais recente com maior ID)
        novaLinha.insertCell(0).textContent = totalRegistros - index;
        
        novaLinha.insertCell(1).textContent = registro.operador;
        const [data, hora] = registro.dataHora.split(', ');
        novaLinha.insertCell(2).textContent = data;
        novaLinha.insertCell(3).textContent = hora;
        novaLinha.insertCell(4).textContent = registro.temperatura;
        novaLinha.insertCell(5).textContent = registro.pressao;
        novaLinha.insertCell(6).textContent = registro.volume;
    });
}


