// Evento para exportar registros para CSV
    
document.getElementById('exportar-excel').addEventListener('click', function () {
    let registros = JSON.parse(localStorage.getItem('registros')) || [];
    if (registros.length === 0) {
        alert('Nenhum registro encontrado para exportar.');
        return;
    }

    // Inverter a ordem dos registros
    registros.reverse();

    let csvContent = "data:text/csv;charset=utf-8,ID;Operador;Data;Hora;Temperatura;Pressao;Volume de Gas\n";
    registros.forEach((registro, index) => {
        const [data, hora] = registro.dataHora.split(', ');
        csvContent += `${index + 1};${registro.operador};${data};${hora};${registro.temperatura};${registro.pressao};${registro.volume}\n`;
    });

    const encodedUri = encodeURI(csvContent);
    const link = document.createElement("a");
    link.setAttribute("href", encodedUri);
    link.setAttribute("download", "registros.csv");
    document.body.appendChild(link);
    link.click();
});

