// Função para calcular o volume de gás
function calcularVolume() {
   const temperatura = parseFloat(document.getElementById('temperatura').value);
    const pressao = parseFloat(document.getElementById('pressao').value);

    // Verifica se temperatura e pressão são valores válidos, incluindo 0
    if (!isNaN(temperatura) && !isNaN(pressao)) {
        const registroEncontrado = tabelaVolumeGas.find(item => item.temperatura === temperatura && item.pressao === pressao);
        if (registroEncontrado) {
            document.getElementById('volume').value = registroEncontrado.volume;
        } else {
            document.getElementById('volume').value = 'Não encontrado';
        }
    }
}


